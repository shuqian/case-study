<?php
	for($i=0,$j=50; $i<100; $i++) {				//双层循环的外层for循环
		while($j--) {							//内层循环while
			if($j==17) {						//判断退出双层循环的条个件
				goto end;						//如果条件成员， 使用goto跳转到end位置，直接退出双层循环
			} else {
				echo "变量i = {$i}, 变量j = {$j}<br>";
			}				
		}  
	}
	
	echo "i = $i";								//本条语句也会被忽留
	
	end:										//为goto语句设置一个自定义标记， 跳转到此结速出双层循环
	
	
	
	

	
	